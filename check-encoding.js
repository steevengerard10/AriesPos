const fs = require('fs');
const text = fs.readFileSync('packages/renderer/src/i18n/index.ts', 'utf8');
const idx = text.indexOf("'nav.estadisticas'");
const valueStart = text.indexOf("'", idx + 20) + 1;
const valueEnd = text.indexOf("'", valueStart);
const value = text.substring(valueStart, valueEnd);
const codes = [...value].map(c => c.codePointAt(0).toString(16));
const result = `VALUE:${value}\nCODES:${codes.join(' ')}\nJSON:${JSON.stringify(value)}\n`;
fs.writeFileSync('check-result2.txt', result, 'utf8');
console.log('Written to check-result2.txt');
